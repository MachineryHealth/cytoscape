<div class="introduction1">
	<h2>What is Cytoscape?</h2>
	<img src="images/intro_2.png" />
	<img src="images/intro_1.png" />
	<p>
		<strong><i>Cytoscape</i></strong> is an open source bioinformatics software platform for <strong><i>
		visualizing</i></strong> molecular interaction networks and biological pathways and 
		<strong><i>integrating</i></strong> these networks with annotations, gene expression 
		profiles and other state data. &nbsp; Although Cytoscape was originally designed for 
		biological research, now it is a general platform for complex network analysis and 
		visualization. &nbsp;  Cytoscape <i>core</i> distribution provides a basic set of features 
		for data integration and visualization. &nbsp; Additional features are available as 
		<strong><i>plugins</i></strong>. &nbsp; Plugins are available for network and molecular 
		profiling analyses, new layouts, additional file format support, scripting, and connection 
		with databases. &nbsp; Plugins may be developed by anyone using the Cytoscape open API based on 
		<a href="http://www.java.com/en/" target="_blank">Java&#8482;</a> technology and plugin 
		<a href="community.php"> community development</a> is encouraged.  Most of the 
		plugins are <a href="plugins/index.php">freely available</a>.
	</p>
</div>

<div class="introduction">

	<h2>Features</h2>
	
	<h3>Data Integration</h3>
	<div id="block1">
	<h4>Supports Many Standards</h4>
	<img src="images/intro_1.png" />
	<img src="images/intro_1.png" />
	<p>
		Cytoscape supports a lot of standard network and annotation file formats including: SIF 
		(Simple Interaction Format), GML, XGMML, BioPAX, PSI-MI, SBML, OBO, and Gene Association.  
		Delimited text files and MS Excel&#8482; Workbook are also supported and you can import 
		data files, such as expression profiles or GO annotations, generated by other applications 
		or spreadsheet programs.  Using this feature, you can load and save arbitrary attributes 
		on nodes, edges, and networks.  For example, input a set of custom annotation terms for 
		your proteins, create a set of confidence values for your protein-protein interactions.
	</p>
	</div>
	<div id="block1">
	<h4>Web Service Clients</h4>
	
	<img src="images/intro_1.png" />
	<p>
		Cytoscape works as a web service client.  This means Cytoscape can directly connect to 
		the external public databases and imports network and annotation data.  Currently, Pathway 
		Commons, IntAct, BioMart, NCBI Entrez Gene, and PICR are supported.  And we continue to 
		develop new service clients for popular databases.
	</p>
	</div>
	
	<div id="block1">
	<h4>Interoperability</h4>
	<img src="images/intro_1.png" />
	<p>
		Since Cytoscape supports import/export standard file formats, you can easily put Cytoscape 
		into your workflow.  For example, if you have a network data generated by igraph or R, 
		Cytoscape can load the file as a text table and you can export it in PSI-MI format for 
		other bioinformatics tools or your own programs/scripts.
	</p>
	</div>
	
	<div id="block1">
	<h4>Session File</h4>
	<img src="images/intro_1.png" />
	<p>
		You can save your work by a single click.  All of settings, data files, and visualizations 
		are packed in a session file.  It is called <i>Cytoscape Session (.cys) file</i>.  
		Cytoscape Session file includes networks, attributes (for node/edge/network), Desktop 
		states (selected/hidden nodes and edges, window sizes), Properties, some plugin states, 
		and Visual Styles.  
	</p>
	</div>
	
	<h3>Visualization</h3>
	
	<div id="block1">
	<h4>VizMapper&#8482;</h4>
	<img src="images/intro_1.png" />
	<img src="images/intro_1.png" />
	<p>
		Customize network data display using powerful <strong><i>Visual Styles</i></strong>.  
		View a superposition of gene expression ratios and p-values on the network. &nbsp;Expression data can be mapped to node color, label, border thickness, or border color, etc. according to user-configurable colors and visualization schemes.

	</p>
	</div>
	
	<div id="block1">
	<h4>Image Export</h4>
	<img src="images/intro_1.png" />
	<p>
		You can export networks as publishiable-quality images.  Suppports PDF, EPS, SVG, PNG, 
		and JPEG.
	</p>
	</div>
	
	<div id="block1">
	<h4>Layout</h4>
	<img src="images/intro_1.png" />
	<p>
		Layout networks in two dimensions. &nbsp;A variety of layout algorithms are available, 
		including cyclic, tree, force-directed, edge-weight, and yFiles Organic layouts.  You can 
		also use Manual Layout tools similar to other graphics application user interface.
	</p>
	</div>
	
	<div id="block1">
	<h4>Browsing</h4>
	<img src="images/intro_1.png" />
	<p>
		Zoom in/out and pan for browsing the network.  Use the network manager to easily organize 
		multiple networks.  And this structure can be saved in a session file.  Use the <i>bird's 
		eye view</i> to easily navigate large networks.  Easily navigate large networks (100,000+ nodes and edges) by efficient rendering engine.
	</p>
	</div>

	<h3>Analysis</h3>
	<div id="block1">
	<h4>Filter</h4>
	<img src="images/intro_1.png" />
	<p>
		<strong>Filter the network</strong> to select subsets of nodes and/or interactions based 
		on the current data. &nbsp;For instance, users may select nodes involved in a threshold 
		number of interactions, nodes that share a particular GO annotation, or nodes whose gene 
		expression levels change significantly in one or more conditions according to p-values 
		loaded with the gene expression data.
	</p>
	</div>
	
