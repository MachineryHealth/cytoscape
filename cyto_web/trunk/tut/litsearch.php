<h2>Using the Agilent Literature Search plugin</h2>
</center>

<p>In cases where there are few measured interactions, text mining
can be a useful mechanism for inferring network data. The Agilent
Literature Search plugin in Cytoscape provides a flexible,
interactive platform for mining text, and assessing the results in
a network context. Here we shall explore the use of this
plugin.</p>

<p>This plugin searches public literature repositories such as
PubMed for articles matching user-specified queries, and then
builds a network based on putative associations suggested in the
text of the artices. Putative associations are sentences of two or
more gene or protein names, and verbs that suggest interaction such
as "catalyzes", "is repressed by", or "regulates". For
a description of the algorithm, see the following <a href=
"http://www.ncbi.nlm.nih.gov/entrez/query.fcgi?cmd=Retrieve&amp;db=pubmed&amp;dopt=Abstract&amp;list_uids=15608051&amp;query_hl=1&amp;itool=pubmed_docsum">
article</a></p>

<p>If you have completed the basic Cytoscape tutorials, in this
tutorial you will</p>

<ul>
<li>Learn how to apply the Agilent Literature Search plugin to
build a network of putative molecular associations from a set of
literature search terms</li>

<li>Explore the associations generated by the plugin, and learn how
to remove any that you judge to be in error.</li>

<li>Learn how to refine your search by using context
information.</li>
</ul>

This tutorial and accompanying lectures were delivered at <a href=
"http://www.csc.fi/suomi/info/index.phtml.en">CSC</a>, the Finnish
IT center for science. The lecture slides of background material
are available <a href="litsearch.ppt">here</a> and an accompanying
video presentation is available <a href=
"http://rm.tv.funet.fi:8080/ramgen/fi/csc/kurssit/2005/cytoscape/_cytos03.rm">
here</a> courtesy of CSC. 

<p><b><i>Important:</i></b> Public literature repositories such as
PubMed are always changing. The illustrations shown here are based
on the public literature databases as they existed when this
document was written. At another time, after more papers have been
published, the composition of the databases will change, and the
exact search results will change. Consequently, <b>your own search
results will probably not look exactly like the ones shown
here</b>.</p>

<p><br>
</p>

<p>If you have not already done so, download and install Cytoscape
on your local computer, following the instructions given in the
Cytoscape manual.</p>

<p>Download and install the Agilent Literature Search plugin, as
follows:</p>

<ol>
<li>Go to the <a href=
"http://www.agilent.com/labs/research/mtl/projects/sysbio/sysinformatics/downloadv2.html">
plugin license page</a></li>

<li>Read the license agreement and fill out the short form below,
clicking the checkbox to accept the terms of the agreement.</li>

<li>Click on the <b>Proceed to Download</b> button, proceeding from
there to the plugin download page.</li>

<li>Select the installer for your platform (Windows, Mac OS X, or
Linux), click on the appropriate download link, and follow the
installation instructions provided via the adjacent <b>View</b>
link.</li>

<li>If you are currently running Cytoscape, exit.</li>
</ol>

