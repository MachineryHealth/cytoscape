Abstracts of their talks and resumes of the Speakers can be found <a href="abstracts.php"><b>here</b></a><br>
In addition, the Cytoscape community organizes a developers retreat each year to establish a roadmap 
for Cytoscape development. Attendees of the symposium may also be interested in Demo Day and Cytoscape Tutorials on Nov 7th.
<br> 

The goal of the retreat is threefold:

<ol>

  <li>Establish a development roadmap for the next year </li>

  <li>Involve the large user community in Europe in Cytoscape
development</li>

  <li>Gather user feedback and use it to plan future development.
  </li>

</ol>

We want to reach these goals in a retreat spanning four days. The first
and last day (6 and 9 November)are mainly intended for the Cytoscape core development
team and other developers who write their own Cytoscape plugins. The
second and third days are aimed at a wide audience of molecular
biologists and bioinformaticians in the Netherlands, Europe and
internationally.
