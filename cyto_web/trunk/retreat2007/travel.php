  <p>
  <h3>Getting there</h3>
  
  <h4>By plane</h4>
  <a href="http://www.schiphol.com" target="_blank">Amsterdam Schiphol Airport</a> is situated 15 km south-west 
  of the city. Train transport to and from Schiphol is convenient, simple and fast, with the Schiphol train 
  station located directly below the main lobby of the airport. <a href="http://www.iamsterdam.com/visiting_exploring/getting_there_around/schiphol_airport_by" target="_blank">Here</a> 
  are the finer details.
  When leaving Amsterdam, give yourself enough time to get to your plane and through security! (Especially when flying to 
  the United States) Schiphol is a large airport - be there at least an hour in advance.

  <h4>Rail</h4>
  Amsterdam is served by eight stations of the Dutch Railways (<a href="http://www.ns.nl" target="_blank">NS</a>). Five are intercity stops: Amsterdam Sloterdijk, Amsterdam 
  Amstel, Amsterdam Zuid, Amsterdam Bijlmer/Arena and Amsterdam Centraal. Amsterdam Centraal is an international train 
  station. From the station there are regular services with destinations in Belgium, France, Germany and Switzerland.

  <h3>Getting around</h3>
  <h4>Bike</h4>
  Amsterdam seems to be built for bikes, which are probably one of the most convenient and fun ways to get around 
  the compact city with its narrow streets. Nearly half of all traffic movements in Amsterdam are by bike, 
  so it makes sense that you should join in by renting a bike.
  <ul>
  <li><a href="http://www.starbikesrental.com/" target="_blank">Typical black "Oma" (granny) bikes </a></li>
  <li><a href="http://www.amsterdam.info/transport/bike-rentals/" target="_blank">A list of more bike rentals</a></li>
  </ul>

  <h4>Public Transport</h4>
  Public transport in Amsterdam, operated by the <a href="http://www.gvb.nl/english/default.asp" target="_blank">GVB</a>, Connexxion, Arriva, and Nederlandse 
  Spoorwegen, consists of numerous bus, metro and tram lines. An overview of maps is <a href="http://www.gvb.nl/english/travellers/maps/index.html" target="_blank">here</a>
  In all public transport you can use the "strip-ticket"; the ins and outs of which are described at <a href="http://www.gvb.nl/english/travellers/ticketsandfares/strip-ticket.html" target="_blank">the gvb site</a>.
  Apart from the metro stations they are available at post offices, Albert Heijn supermarkets and most tobacconists in Amsterdam.
  

  <h4>Taxis</h4>
  Taxis in Amsterdam are plentiful but expensive. Find the details <a href="http://www.iamsterdam.com/visiting_exploring/getting_there_around/taxis" target="_blank">here</a>.
  
