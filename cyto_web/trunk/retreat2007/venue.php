  <h3>Route Description</h3>

  <h4>Subway</h4>
  <p>
  This is by far the easiest way to reach the AMC; from subway stop <b>Holendrecht</b> it is a 250 meter (covered) walk to the main entrance of the AMC.
  In the AMC you will be directed to the proper location of the event.
  Take line <b>50</b> (Isolatorweg – Gein) or line <b>54</b> (Central Station – Gein) direction <b>Gein</b>, get off at Holendrecht.<br>
  <br>
  &rarr;Download the <a href="pdf/AMC_Metro.pdf" target="_blank">Subway map(pdf), indicating Holendrecht</a>
  </p>  
 
  <h4>Train</h4>
  <p>
  All intercity stations in Amsterdam have possibilities to transfer to the subway system.
  </p>  
 
  <h4>Bus</h4>
  <p>
  Busses 59, 60 and 61 have a stop at the hospital entrance. Bus 29 stops at the underground station Holendrecht (see 
  information underground)
  Bus 158 (from Almere) stops at subway station Holendrecht.
  Bus 120 (from Utrecht) and 126 (from Wilnis) both stop at the Psychiatric Centre as well as the subway station 
  Holendrecht.
  Bus 137 (from Hilversum), bus 174 (from Weesp to Haarlem, via Schiphol and Hoofddorp) and bus 175 (from Diemen to 
  Amstelveen) all stop at NS-station Bijlmer.
  From there take subway line 50 or 54 to Gein, get off at Holendrecht (see information underground)
  </p>  
 
  <h4>By car</h4>
  <p>
  The AMC is signposted in both directions of the motorways A2 and A9. ATTENTION: The parking at the main entrance of the 
  hospital is paid parking (P0) (09.00-22.00 hrs): &euro; 1,00 for the first three hours, and &euro; 0,50 for every additional half 
  hour.<br>
  <br> 
