<h3><a name="benno">Jean-Daniel Fekete</a></h3>
<br>

<table class="programme">
 <tr>
  <td class="subheader"><p>Position/Title/Institute</p></td>
 </tr>
 <tr>
  <td><p>Research Director - Head of the AVIZ Team - INRIA, France
</p></td>
 </tr>
 <tr>
  <td class="subheader"><p>Talk Title</p></td>
 </tr>
 <tr>
  <td><p>Visualizing Dense Networks with Enhanced and Hybrid Matrices</p></td>
 </tr>
 <tr>
  <td class="subheader"><p>Talk Abstract</p></td>
 </tr>
 <tr>
  <td><p>
   The Node-Link Diagram is the most popular visual representation for networks. However, when a network becomes dense or large, they become unreadable. The Matrix representation is known but not used by most network visualization systems. We showed in 2003 that it outperforms the Node-Link representation for large and dense networks for most low-level tasks, except path-related ones.
<br>In this talk, we present the state of our research regarding the enhancement of visual matrices including MatrixExplorer, a dual-representation system for exploring large networks, MatLink, an augmented matrix representation and NodeTrix, a hybrid representation combining node-links and matrices.
<br>We also argue for the addition of these representations in network visualization systems to complement and augment their analytical and exploration power.	  
  </p></td>
 </tr>
