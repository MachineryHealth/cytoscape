<h3><a name="sorger">Peter Sorger</a></h3>
<br>

<table class="programme">
 <tr>
  <td class="subheader"><p>Position/Title/Institute</p></td>
 </tr>
 <tr>
  <td><p>Professor of Systems Biology, Center for Cell Decision Processes, Dept. of Biological Engineering, MIT and Department of Systems Biology, Harvard Medical School
</p></td>
 </tr>
 <tr>
  <td class="subheader"><p>Talk Title</p></td>
 </tr>
 <tr>
  <td><p>Modeling Mammalian Death and Survival Pathways</p></td>
 </tr>
 <tr>
  <td class="subheader"><p>Talk Abstract</p></td>
 </tr>
 <tr>
  <td><p>
Caspases, the proteases that dismantle apoptotic cells, normally switch from off to on in an all-or-none process that enforces an unambiguous choice between life and death.  To understand the operation of this switch in quantitative terms we have constructed a mass-action mathematical model of receptor-mediated cell death triggered by TNF and TRAIL based on known reaction pathways and trained the model on data from single cells perturbed by protein depletion, over-expression, or inhibition.  We find that receptor-mediated cell death is characterized by sudden and efficient cleavage of caspase substrates (over a 10-15 min period), but only after a remarkably long delay (1 to 12 hr), whose duration and variance depend on ligand dose and identity.  Thus, caspase regulatory pathways simultaneously achieve snap-action activation, long and variable delay and high efficiency; it is not sufficient that all processes be fast.  We hypothesize that variable delay generates a tunable dose-dependent behavior at a population level from a binary decision at a single-cell level.
<br>With the help of three newly designed single cell fluorescence reporters for initiator and executioner caspases and for mitochondrial membrane permeabilization, we have begun to dissect the trigger comprising pro-death and pro-survival BH3 proteins in the mitochondrial membrane. As suggested nearly a decade ago by Korsmeyer and colleagues, this trigger involves a competition between pore forming and pore-inhibiting proteins. While it would seem logical that the trigger would fire when activators exceed inhibitors modeling and measurement support the idea that the actual threshold is crossed at a ratio of ~0.6 to 0.9, reflecting the dynamics of the "race" between the two classes of regulators.  Direct competition between inhibitory and activating protein-protein binding makes for a very poor switch and other processes are involved in snap-action control of caspases, including slow transport of active Bax from the cytosol to mitochondria, the concentrating effects of moving from 3D to 2D diffusion and high-order assembly of Bax-containing pores.
<br>Once mitochondria depolarize a powerful feed-forward circuit, involving cytosolic translocation of SMAC/DIABLO and cytochrome C leads to activation of executioner caspases. Prior to that point, for up to 12 hr in the cases of low TRAIL doses active initiator caspases accumulate steadily, leading to cleavage of Caspase 3 when is then strongly repressed. The generation of such a potent and lethal protease in cells that not yet committed to death is remarkable.  Moreover, under some conditions, it gives rise to unexpected failure modes in which cells commit to partial death and survive with damage to their genomes.  Such an event is expected to cause genomic instability, consistent with previous suggestions that the chromosomal translocations typical of lymphoma might be caused by failures of apoptosis.
  </p></td>
 </tr>
 <tr>
  <td class="subheader"><p>Biography / Awards</p></td>
 </tr>
 <tr>
  <td><p>
Peter Sorger, Ph.D is a Professor of Systems Biology at Harvard Medical School and holds a joint appointment in MIT's Dept. of Biological Engineering and Center for Cancer Research. He received his A.B. from Harvard College, Ph.D. from Trinity College Cambridge, U.K. and trained as a postdoctoral fellow with Harold Varmus and Andrew Murray at the University of California, San Francisco. Sorger was co-founder of the MIT systems biology program CSBi, Merrimack Pharmaceuticals and Glencoe Software and serves on the scientific advisory and corporate boards of several other technology companies. He is currently Chair of the CSF study section of the NIH and Director of the NIH-NIGMS CDP Center for Systems Biology.  
  </p></td>
 </tr>
 </table>
 
