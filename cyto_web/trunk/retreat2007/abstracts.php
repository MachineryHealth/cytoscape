 </tr>
 <tr>
  <td><p>President, Institute for Systems Biology</p></td>
 </tr>
 <tr>
  <td class="subheader"><p>Talk Title</p></td>
 </tr>
 <tr>
  <td><p>Biological networks and disease</p></td>
 </tr>
 <tr>
  <td class="subheader"><p>Talk Abstract</p></td>
 </tr>
 <tr>
  <td><p>
  I will discuss a systems approach to disease using as an example prion infection in mice.  I will show how the dynamics of protein networks derived from the dynamic analyses of brain transcriptomes superimposed on protein/protein networks from the literature and integrated together with various types of phenotypic data actually explain much of the known dynamics of the disease.  I will also show how this disease-perturbed network view of disease leads to a new approach to blood diagnostics that promises to transform early disease diagnosis and following responses to therapies.  I will also show how this approach is driving the development of new measurement technologies employing microfluidics and nanotechnology.  These systems approaches together with the new measurement technologies are driving the emergence of a new medicine that will be predictive, personalized, preventive and participatory (P4).  I will also discuss the long-term implications of P4 medicine for world health.
  </p></td>
 </tr>
 <tr>
  <td class="subheader"><p>Biography / Awards</p></td>
 </tr>
 <tr>
  <td><p>
Dr. Hood's research has focused on the study of molecular immunology, biotechnology, and genomics. His professional career began at Caltech where he and his colleagues pioneered four instruments - the DNA gene sequencer and synthesizer, and the protein synthesizer and sequencer - which comprise the technological foundation for contemporary molecular biology. In particular, the DNA sequencer has revolutionized genomics by allowing the rapid automated sequencing of DNA, which played a crucial role in contributing to the successful mapping of the human genome during the 1990s. In 1992, Dr. Hood moved to the University of Washington as founder and Chairman of the cross-disciplinary Department of Molecular Biotechnology. In 2000, he co-founded the Institute for Systems Biology in Seattle, Washington to pioneer systems approaches to biology and medicine. Most recently, Dr. Hood's lifelong contributions to biotechnology have earned him the prestigious 2004 Biotechnology Heritage Award, and for his pioneering efforts in molecular diagnostics the 2003 Association for Molecular Pathology (AMP) Award for Excellence in Molecular Diagnostics. In 2006 he received the Heinz Award in Technology, the Economy and Employment for his extraordinary breakthroughs in biomedical science at the genetic level. In 2007 he was elected to the Inventors Hall of Fame (for the automated DNA sequencer). He has published more than 600 peer-reviewed papers, received 14 patents, and has co-authored textbooks in biochemistry, immunology, molecular biology, and genetics and is just finishing a text book on systems biology. In addition, he coauthored with Dan Keveles a popular book on the human genome project-The Code of Codes. Dr. Hood is a member of the National Academy of Sciences, the American Philosophical Society, the American Academy of Arts and Sciences, the Institute of Medicine and the National Academy of Engineering. Indeed, Dr. Hood is one of 7 (of more than 6000) scientists elected to all three academies (NAS, NAE and IOM). Dr. Hood has also played a role in founding more than 14 biotechnology companies, including Amgen, Applied Biosystems, Systemix, Darwin and Rosetta. He is currently pioneering systems medicine and the systems approach to disease. 
Awards:
<br>2005 Heinz Award for Technology,for the Economy and Employment Development and commercialization of high throughput biology (automated protein and DNA sequencing). 
