<h1>Background</h1>

<p>Cytoscape Web is actively developed at the
<a href="http://utoronto.ca">University of Toronto</a>, in
the <a href="http://tdccbr.med.utoronto.ca">Donnelly Centre for Cellular and Biomolecular Research</a>,
in the labs of <a href="http://baderlab.org">Gary Bader</a> and
<a href="http://morrislab.med.utoronto.ca">Quaid Morris</a>.  Cytoscape Web development is funded
by <a href="http://genomecanada.ca">Genome Canada</a>, through the
<a href="http://ontariogenomics.ca/">Ontario Genomics Institute</a> (2007-OGI-TD-05).</p>

<p>Cytoscape Web was created as a result of the need for a visualisation interface for the
<a href="http://genemania.org">GeneMANIA</a> project at the University of Toronto. Cytoscape Web
was developed as an independent component so that it could be reused by others in the
<a href="/about/license">spirit of open source software</a>.</p>



<h1>Client applications</h1>

<p>Cytoscape Web is currently being used by a gene function prediction server,
<a href="http://genemania.org">GeneMANIA</a>.
The data provided to Cytoscape Web in GeneMANIA is unique to every user query, and easily
modified with each new query.  The rendering speed of Cytoscape Web allows for dynamic user
interaction.</p>

