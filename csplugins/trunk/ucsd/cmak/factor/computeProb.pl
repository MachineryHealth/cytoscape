sub p
{
    my $type = shift @_;
    my $i = shift @_;
    my $p1 = shift @_;
    my $p2 = shift @_;
    my $p3 = shift @_;

    print $type . " " . $i . " " . $p1 . " " . $p2;

    if(defined($p3))
    {
	print " " . $p3;
    }

    print "\n";
}

