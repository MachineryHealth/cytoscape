public class AlgorithmException extends Exception
{
    public AlgorithmException()
    {
        super();
    }

    public AlgorithmException(String msg)
    {
        super(msg);

    }

    public AlgorithmException(String msg, Throwable t)
    {
        super(msg, t);
    }
    
    public AlgorithmException(Throwable t)
    {
        super(t);
    }


}
