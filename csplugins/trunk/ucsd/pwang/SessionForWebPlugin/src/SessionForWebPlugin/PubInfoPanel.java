    // Check if a file contains a URL
    private boolean containsURL(File pFile){
    	String url = "";
    	try {
            BufferedReader in = new BufferedReader(new FileReader(pFile));
            String str;
            while ((str = in.readLine()) != null) {
                url += str;
            }
            in.close();
        } catch (Exception e) {
        }

       try {
            URL theURL = new URL(url);        	
        }
        catch(Exception e){
        	return false;
        }
        
    	return true;
    }
    
