    class PDFFilter extends FileFilter {
    	public boolean accept(File f){
    		if (f.isDirectory()){
    			return true;
    		}
    		
    		String ext = Utils.getExtension(f);
    		if (ext != null && ext.equalsIgnoreCase("pdf")){
    			return true;
    		}
    		return false;
    	}
    	public String getDescription(){
    		return "pdf";
    	}
    }
