    private Set<DataSource> getTestSupportedIDSources() {
        Set<DataSource> ret = new java.util.HashSet();
        for (int i=0; i<3; i++) {
            ret.add(DataSource.register("s"+i, "source"+i, null, null, null, false, false, null));
        }
        return ret;
    }

